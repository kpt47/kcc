// สคริปต์ครั้งเดียว: เตรียมไฟล์โลโก้จากภาพต้นฉบับ public/logos/source/logoapp-original.png
// (แถบ 3 ตราเรียงกัน) — ตัดเฉพาะตราขวา (โครงการ กข.คจ. ที่ยืนยันชื่อแน่ชัดแล้ว) เป็นไอคอนสี่เหลี่ยม
// ใช้เป็น favicon/แบรนด์มาร์ก และเก็บภาพรวมทั้งแถบไว้อีกไฟล์สำหรับใช้ตรงจุดที่ควรโชว์ตราราชการเรียงแถว
// (หน้า login, หัวกระดาษ PDF) รันใหม่ด้วย `node scripts/prepare-logo-assets.mjs` ถ้าเปลี่ยนภาพต้นฉบับ
import { readFileSync, writeFileSync, mkdirSync, copyFileSync } from "node:fs";
import path from "node:path";
import sharp from "sharp";

const root = path.resolve(import.meta.dirname, "..");
const sourcePath = path.join(root, "public/logos/source/logoapp-original.png");
const programLogoPath = path.join(root, "public/logos/logo-kbkj-program.png");
const sealsRowPath = path.join(root, "public/logos/seals-row.png");
const iconPath = path.join(root, "src/app/icon.png");
const appleIconPath = path.join(root, "src/app/apple-icon.png");
const assetsPath = path.join(root, "src/lib/pdf/assets/sealAssets.ts");

// ตราขวา (กข.คจ.) อยู่ประมาณ 1 ใน 3 สุดท้ายของภาพต้นฉบับ 400x140
const RIGHT_SEAL_CROP = { left: 266, top: 0, width: 134, height: 140 };

async function main() {
  mkdirSync(path.dirname(programLogoPath), { recursive: true });
  mkdirSync(path.dirname(assetsPath), { recursive: true });

  await sharp(sourcePath)
    .extract(RIGHT_SEAL_CROP)
    .resize(512, 512, { fit: "contain", background: { r: 0, g: 0, b: 0, alpha: 0 } })
    .png()
    .toFile(programLogoPath);

  copyFileSync(programLogoPath, iconPath);
  copyFileSync(programLogoPath, appleIconPath);

  // ภาพรวมทั้งแถบ 3 ตรา — คัดลอกไม่ตัด
  await sharp(sourcePath).png().toFile(sealsRowPath);

  const sealsRowBase64 = readFileSync(sealsRowPath).toString("base64");

  writeFileSync(
    assetsPath,
    `// AUTO-GENERATED by scripts/prepare-logo-assets.mjs — ห้ามแก้ไฟล์นี้ด้วยมือ
// รันสคริปต์ใหม่ถ้าเปลี่ยนภาพต้นฉบับ public/logos/source/logoapp-original.png
export const SEALS_ROW_BASE64 = "${sealsRowBase64}";
`
  );

  console.log("เสร็จแล้ว:");
  console.log(" -", path.relative(root, programLogoPath));
  console.log(" -", path.relative(root, iconPath));
  console.log(" -", path.relative(root, appleIconPath));
  console.log(" -", path.relative(root, sealsRowPath));
  console.log(" -", path.relative(root, assetsPath));
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});

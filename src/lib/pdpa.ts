/** ตรวจสอบว่า user คนนี้ยินยอมนโยบายเวอร์ชันปัจจุบันแล้วหรือยัง */
export async function hasAcceptedCurrentPdpa(userId: number): Promise<boolean> {
  const consent = await prisma.pdpaConsent.findFirst({
    where: { userId, policyVersion: PDPA_POLICY_VERSION },
    select: { id: true },
  });
  return consent != null;
}

function extractIp(request: Request): string | null {
  const forwardedFor = request.headers.get("x-forwarded-for");
  if (forwardedFor) return forwardedFor.split(",")[0].trim();
  return request.headers.get("x-real-ip");
}

/** บันทึกความยินยอมนโยบายเวอร์ชันปัจจุบันของ user — เรียกจาก /api/pdpa/accept เท่านั้น */
export async function recordPdpaConsent(userId: number, request: Request) {
  await prisma.pdpaConsent.create({
    data: { userId, policyVersion: PDPA_POLICY_VERSION, ipAddress: extractIp(request) },
  });
}
